/**
 * メモリ管理シミュレータの型定義モジュール
 *
 * OSのメモリ管理にはセグメント方式とページ方式の2つの主要な手法がある。
 *
 * ■ セグメント方式:
 *   プログラムを論理的な単位（コード、データ、スタック、ヒープ）に分割し、
 *   各セグメントにベースアドレスとリミット（上限サイズ）を設定する。
 *   論理アドレスは「セグメント番号:オフセット」で表され、
 *   物理アドレスは base + offset で計算される。
 *   セグメントのサイズが可変であるため外部断片化（external fragmentation）が発生しやすい。
 *
 * ■ ページ方式:
 *   仮想アドレス空間と物理メモリを固定サイズのページ/フレームに分割する。
 *   ページテーブルが仮想ページ番号から物理フレーム番号への対応を保持する。
 *   固定サイズのため外部断片化は起きないが、内部断片化（ページ末尾の未使用領域）が
 *   発生する可能性がある。TLB（Translation Lookaside Buffer）はページテーブルの
 *   キャッシュとして機能し、アドレス変換を高速化する。
 */

/**
 * メモリ管理方式の種類
 * - "segment": セグメント方式（可変長のメモリ領域による管理）
 * - "paging": ページ方式（固定長のページ/フレームによる管理）
 */
export type MemoryScheme = "segment" | "paging";

/* ========================================================================
 * セグメント方式の型定義
 *
 * セグメント方式では、プログラムの論理構造に沿ってメモリを分割する。
 * 各セグメントはベースレジスタ（開始位置）とリミットレジスタ（サイズ上限）を持ち、
 * ハードウェアがアクセスごとにリミットチェックと保護チェックを行う。
 * x86アーキテクチャのGDT/LDTはこの方式の実装例である。
 * ======================================================================== */

/**
 * セグメントの種類
 * - "code": 実行可能な命令コードを格納する領域（通常は読み取り専用）
 * - "data": グローバル変数や静的データを格納する領域
 * - "stack": 関数呼び出しのスタックフレーム（ローカル変数、戻りアドレス等）を格納する領域
 * - "heap": malloc/freeで動的に確保・解放されるヒープ領域
 */
export type SegmentType = "code" | "data" | "stack" | "heap";

/**
 * セグメントテーブルのエントリ
 *
 * セグメントテーブルはOSが管理するデータ構造で、各セグメントの物理メモリ上の
 * 位置とサイズ、およびアクセス権限を記録する。CPUがメモリアクセスを行う際、
 * セグメント番号をインデックスとしてこのテーブルを参照し、アドレス変換と保護チェックを行う。
 */
export interface SegmentEntry {
  /** セグメント番号（セグメントテーブルのインデックスとして使用される一意な識別子） */
  id: number;
  /** セグメント名（可視化時の表示用。例: "コード", "データ", "スタック"） */
  name: string;
  /** セグメントの種類（code/data/stack/heapのいずれか） */
  type: SegmentType;
  /**
   * ベースアドレス（物理メモリ上の開始位置）
   * 物理アドレス = base + offset で算出される
   */
  base: number;
  /**
   * セグメントサイズ（リミット値）
   * オフセットがこの値以上の場合、セグメンテーションフォールトが発生する。
   * これによりセグメント境界を超えた不正アクセスを防止する。
   */
  limit: number;
  /** 読み取り許可（falseの場合、読み取りアクセスで保護違反となる） */
  readable: boolean;
  /** 書き込み許可（コードセグメントでは通常false。書き込み不可ページへの書き込みはCopy-on-Writeの実装にも利用される） */
  writable: boolean;
  /** 実行許可（データ/スタック領域では通常false。NXビットによるDEP保護に相当） */
  executable: boolean;
  /**
   * メモリ上に存在するか（presentビット）
   * falseの場合、セグメントはディスクにスワップアウトされており、
   * アクセス時にセグメントフォールトが発生してOSがスワップインを行う。
   */
  present: boolean;
}

/**
 * セグメント方式の論理アドレス
 *
 * セグメント方式ではアドレスが「セグメント番号:オフセット」の2要素で構成される。
 * x86のリアルモードでは segment:offset 形式が使われ、
 * プロテクトモードではセグメントセレクタがGDT/LDTのインデックスを指す。
 */
export interface SegmentAddress {
  /** セグメント番号（セグメントテーブルへのインデックス） */
  segment: number;
  /**
   * セグメント内オフセット
   * セグメントの先頭からのバイト距離。limit未満でなければならない。
   */
  offset: number;
}

/* ========================================================================
 * ページ方式の型定義
 *
 * ページ方式では、仮想アドレス空間を固定サイズの「ページ」に、
 * 物理メモリを同サイズの「フレーム」に分割する。
 * ページテーブルがページ番号→フレーム番号の対応を管理し、
 * TLBがその高速キャッシュとして機能する。
 * 現代のOSは多段ページテーブル（x86-64では4段）を使用するが、
 * このシミュレータでは単段ページテーブルで簡略化している。
 * ======================================================================== */

/**
 * ページテーブルエントリ（PTE: Page Table Entry）
 *
 * ページテーブルの各エントリは、仮想ページ番号と物理フレーム番号の対応関係、
 * およびページの属性フラグを保持する。実際のx86 PTEは64ビットで、
 * present, R/W, U/S, PWT, PCD, accessed, dirty, PS, global, NXなどのビットフラグを含む。
 */
export interface PageTableEntry {
  /** 仮想ページ番号（仮想アドレス空間内でのページの位置） */
  pageNumber: number;
  /**
   * フレーム番号（物理メモリ上のページフレームの位置）
   * 物理アドレス = frameNumber × pageSize + offset で算出される。
   * present=falseの場合、この値はディスク上の位置を示すこともある。
   */
  frameNumber: number;
  /**
   * 存在ビット（present bit）
   * trueの場合、ページが物理メモリ上に存在する。
   * falseの場合、ページはディスク（スワップ領域）に退避されており、
   * アクセス時にページフォールト（#PF例外）が発生する。
   * OSのページフォールトハンドラがディスクからページを読み込む（デマンドページング）。
   */
  present: boolean;
  /**
   * ダーティビット（dirty bit / modified bit）
   * ページへの書き込みが行われるとハードウェアが自動的にセットする。
   * ページ置換時、ダーティなページはディスクに書き戻す必要がある。
   * クリーンなページは単に破棄できるため、I/Oコストを削減できる。
   */
  dirty: boolean;
  /**
   * 参照ビット（referenced bit / accessed bit）
   * ページへの読み書きが行われるとハードウェアがセットする。
   * LRU近似のページ置換アルゴリズム（Clock算法など）で使用される。
   * OSが定期的にクリアし、再度セットされたかで最近の使用状況を判定する。
   */
  referenced: boolean;
  /** 読み取り許可（falseの場合、読み取りアクセスで保護違反例外が発生） */
  readable: boolean;
  /** 書き込み許可（falseの場合、書き込みアクセスで保護違反例外が発生。Copy-on-Writeにも利用） */
  writable: boolean;
  /** 実行許可（NXビットの反転に相当。falseの場合、実行アクセスで保護違反が発生） */
  executable: boolean;
}

/**
 * TLBエントリ（Translation Lookaside Buffer）
 *
 * TLBはページテーブルのキャッシュで、CPU内のハードウェアが管理する連想メモリ（CAM）である。
 * ページテーブルはメインメモリ上にあるため参照にメモリアクセスが必要だが、
 * TLBにキャッシュされたエントリは1クロックで参照でき、アドレス変換を大幅に高速化する。
 * TLBミス時はページテーブルウォークが発生し、結果がTLBに登録される。
 * TLBが満杯の場合、LRU（Least Recently Used）などの置換アルゴリズムで古いエントリを追い出す。
 */
export interface TlbEntry {
  /** 仮想ページ番号（検索キーとして使用される） */
  pageNumber: number;
  /** 対応する物理フレーム番号（キャッシュされた変換結果） */
  frameNumber: number;
  /**
   * 最終アクセス時刻（LRU置換アルゴリズム用）
   * TLBが満杯になった際、この値が最も小さい（最も古い）エントリが置換される。
   */
  lastAccess: number;
}

/**
 * ページ方式の仮想アドレス分解
 *
 * 仮想アドレスは上位ビットの「ページ番号」と下位ビットの「ページ内オフセット」に分解される。
 * 例: ページサイズ256バイト（8ビット）の場合、
 *   仮想アドレス 0x0310 → ページ番号=3, オフセット=0x10
 * ページ番号はページテーブルのインデックスとして使用され、
 * オフセットは物理アドレスにそのまま引き継がれる。
 */
export interface VirtualAddress {
  /** 仮想アドレス全体（分解前の生の値） */
  address: number;
  /** ページ番号（= address ÷ pageSize の商。ページテーブルのインデックス） */
  pageNumber: number;
  /** ページ内オフセット（= address ÷ pageSize の余り。フレーム内での位置） */
  offset: number;
}

/**
 * 物理アドレス分解
 *
 * 物理アドレスも仮想アドレスと同様にフレーム番号とオフセットに分解できる。
 * フレーム番号は物理メモリのどのフレームかを示し、
 * オフセットはそのフレーム内の位置を示す。
 */
export interface PhysicalAddress {
  /** 物理アドレス全体 */
  address: number;
  /** フレーム番号（= address ÷ pageSize の商。物理メモリのフレーム位置） */
  frameNumber: number;
  /** フレーム内オフセット（= address ÷ pageSize の余り） */
  offset: number;
}

/* ========================================================================
 * 共通型定義（セグメント方式・ページ方式の両方で使用）
 * ======================================================================== */

/**
 * アドレス変換ステップ（可視化用）
 *
 * アドレス変換の各過程を記録し、UIで変換フローを段階的に表示するために使用する。
 * 各ステップにはタイプが付与され、UIで色分けやアイコン表示に利用される。
 */
export interface TranslationStep {
  /** ステップの説明文（日本語で変換過程を記述） */
  description: string;
  /**
   * ステップの種類
   * - "info": 一般情報（アドレス分解など）
   * - "lookup": テーブル参照（セグメントテーブル/ページテーブルの検索）
   * - "calc": 計算処理（リミットチェック、保護チェック、物理アドレス算出など）
   * - "success": 変換成功
   * - "error": エラー発生（保護違反、リミット超過など）
   * - "tlb_hit": TLBキャッシュヒット（高速変換）
   * - "tlb_miss": TLBキャッシュミス（ページテーブルウォークが必要）
   * - "page_fault": ページフォールト発生（ディスクI/Oが必要）
   */
  type: "info" | "lookup" | "calc" | "success" | "error" | "tlb_hit" | "tlb_miss" | "page_fault";
  /** 関連する数値・文字列（UIでの詳細表示用） */
  values?: Record<string, number | string | boolean>;
}

/**
 * アドレス変換結果
 *
 * 1回のメモリアクセスに対するアドレス変換の最終結果を表す。
 * 成功時は物理アドレスが、失敗時はエラーメッセージが格納される。
 * stepsには変換の全過程が記録され、UIでのデバッグ表示に利用される。
 */
export interface TranslationResult {
  /** 変換が成功したか（true: 正常に物理アドレスが算出された） */
  success: boolean;
  /** 入力アドレス（セグメント方式ではオフセット、ページ方式では仮想アドレス） */
  inputAddress: number;
  /** 出力された物理アドレス（変換成功時のみ有効） */
  physicalAddress?: number;
  /** エラーメッセージ（変換失敗時のみ。保護違反やフォールトの詳細を含む） */
  error?: string;
  /** 変換過程のステップ一覧（UIでの段階的表示用） */
  steps: TranslationStep[];
}

/**
 * メモリアクセス操作
 *
 * シミュレーションで実行する個々のメモリアクセスを定義する。
 * アクセス種類（読み取り/書き込み/実行）に応じて異なる保護チェックが行われる。
 */
export interface MemoryAccess {
  /**
   * アクセスの種類
   * - "read": データの読み取り（readable権限が必要）
   * - "write": データの書き込み（writable権限が必要）
   * - "execute": 命令の実行（executable権限が必要）
   */
  type: "read" | "write" | "execute";
  /** アクセス先アドレス（セグメント方式ではオフセット、ページ方式では仮想アドレス） */
  address: number;
  /** セグメント番号（セグメント方式のみ。ページ方式では不要） */
  segment?: number;
  /** ラベル（UI表示用の説明文。例: "コード先頭を実行"） */
  label?: string;
}

/**
 * 物理メモリのブロック情報
 *
 * 物理メモリの使用状況を可視化するためのブロック単位の情報。
 * 使用中の領域と空き領域をマップとして表示する。
 * セグメント方式では外部断片化（空き領域が飛び地になる現象）の視覚的な確認に有用。
 */
export interface MemoryBlock {
  /** 物理メモリ上の開始アドレス */
  start: number;
  /** ブロックのサイズ（バイト） */
  size: number;
  /** ブロックのラベル（例: "コード (seg 0)", "フレーム3 (ページ0)", "空き"） */
  label: string;
  /** 使用中か空きか（true: セグメント/フレームが配置されている, false: 未使用の空き領域） */
  used: boolean;
}

/**
 * シミュレーション全体の結果
 *
 * 一連のメモリアクセスを実行した後の総合結果を格納する。
 * アドレス変換の個別結果、物理メモリの使用状況マップ、統計情報を含む。
 */
export interface SimulationResult {
  /** 使用したメモリ管理方式（"segment" または "paging"） */
  scheme: MemoryScheme;
  /** 各アクセスのアドレス変換結果リスト */
  translations: TranslationResult[];
  /** 物理メモリマップ（使用中/空きブロックの一覧） */
  memoryMap: MemoryBlock[];
  /** シミュレーションの統計情報 */
  stats: SimulationStats;
}

/**
 * 統計情報
 *
 * シミュレーション実行後の各種カウンタ。
 * TLBヒット率やページフォールト数はメモリ管理の性能評価に重要な指標となる。
 * 一般的に、TLBヒット率が高いほどアドレス変換のオーバーヘッドが小さい。
 */
export interface SimulationStats {
  /** 総アクセス数（シミュレーションで実行したメモリアクセスの総数） */
  totalAccesses: number;
  /** 成功数（物理アドレスへの変換が正常に完了した回数） */
  successCount: number;
  /** エラー数（保護違反、フォールト等で変換が失敗した回数） */
  errorCount: number;
  /**
   * TLBヒット数（ページ方式のみ）
   * TLBにキャッシュされたエントリで高速に変換できた回数。
   * ヒット率 = tlbHits / (tlbHits + tlbMisses) が高いほど性能が良い。
   */
  tlbHits?: number;
  /**
   * TLBミス数（ページ方式のみ）
   * TLBにエントリがなく、ページテーブルウォークが必要だった回数。
   * ミスが多い場合、ワーキングセットがTLBサイズを超えている可能性がある。
   */
  tlbMisses?: number;
  /**
   * ページフォールト数（ページ方式のみ）
   * アクセスしたページが物理メモリ上に存在せず、ディスクからの読み込みが必要だった回数。
   * ページフォールトはマイクロ秒〜ミリ秒単位のディスクI/Oを伴うため、性能に大きく影響する。
   */
  pageFaults?: number;
  /**
   * セグメントフォールト数（セグメント方式のみ）
   * 不在セグメントへのアクセスや保護違反などで発生したフォールトの回数。
   */
  segmentFaults?: number;
}
