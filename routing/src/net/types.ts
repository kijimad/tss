/**
 * types.ts -- ルーティングシミュレータの型定義
 *
 * ============================================================
 * IPルーティングの基本概念:
 * ============================================================
 *
 * ■ ルーティングテーブル (Routing Table)
 *   各ルータが保持する経路情報のデータベース。
 *   エントリは (宛先ネットワーク, サブネットマスク, ネクストホップ, インターフェース, メトリック) で構成される。
 *   パケットを受信すると、宛先IPアドレスに対して最長プレフィクス一致 (Longest Prefix Match) で
 *   ルーティングテーブルを検索し、転送先を決定する。
 *
 * ■ スタティックルーティング vs ダイナミックルーティング
 *   - スタティック: 管理者が手動で経路を設定。小規模ネットワーク向き。
 *   - ダイナミック: ルーティングプロトコル（RIP, OSPF, BGP等）が自動的に経路を学習・更新。
 *     - ディスタンスベクタ型 (RIP): 隣接ルータと経路情報を定期交換。ホップ数をメトリックとする。
 *     - リンクステート型 (OSPF): ネットワーク全体のトポロジ情報を共有し、ダイクストラ法で最短経路を計算。
 *     - パスベクタ型 (BGP): AS間の経路交換。ASパスを用いてループを防止する。
 *
 * ■ アドミニストレーティブディスタンス (Administrative Distance)
 *   異なるルーティングプロトコルが同一宛先への経路を提供した場合の優先度。
 *   直接接続=0, スタティック=1, OSPF=110, RIP=120, BGP(eBGP)=20 など。
 *
 * ■ CIDR表記とデフォルトルート
 *   - CIDR (例: 192.168.1.0/24) はネットワークアドレスとプレフィクス長を一括表記する方式。
 *   - デフォルトルート (0.0.0.0/0) は他のエントリに一致しないパケットの転送先を指定する。
 *
 * ■ AS (自律システム / Autonomous System)
 *   ISPや企業が管理するネットワークの単位。
 *   AS内はIGP（OSPF等）で、AS間はEGP（BGP）で経路交換を行う。
 *
 * ============================================================
 * 本シミュレータの簡易モデル:
 * ============================================================
 *   - 各ノード = ルータ（IPアドレス付き）
 *   - リンク = 物理接続（コスト = 遅延ms、メトリックとして使用）
 *   - ルーティングテーブル = OSPFに倣いダイクストラ法（リンクステート型）で計算
 *   - パケット = 送信元/宛先IPを持ち、ホップごとにネクストホップへ転送される
 *   - TTL (Time To Live) = ホップごとに1減算し、0になるとパケットを破棄（ループ防止）
 */

/**
 * ルータ
 *
 * ネットワーク上のパケット転送装置を表す。
 * 実際のルータは複数のネットワークインターフェースを持つが、
 * 本シミュレータでは1ルータ1IPアドレスに簡略化している。
 */
export interface Router {
  /** ルータの一意識別子 ("R1", "R2", ...) */
  id: string;
  /** ルータに割り当てられたIPアドレス（例: "10.100.1.1"） */
  ip: string;
  /** 所属するAS（自律システム）名（例: "AS100"）。BGPではこの単位で経路交換が行われる */
  as: string;
  /** Canvas上の描画X座標（ピクセル） */
  x: number;
  /** Canvas上の描画Y座標（ピクセル） */
  y: number;
  /**
   * ルーティングテーブル。
   * ダイクストラ法（OSPF相当のリンクステートアルゴリズム）で計算される。
   * 実際のルータでは `ip route` や `show ip route` コマンドで確認できるものに相当する。
   */
  routingTable: RouteEntry[];
}

/**
 * ルーティングテーブルエントリ
 *
 * 実際のルーティングテーブルは (宛先ネットワーク/マスク, ゲートウェイ, インターフェース, メトリック) だが、
 * 本シミュレータではルータID単位の簡易版を使用する。
 * 各エントリは「ある宛先に到達するには、どのネクストホップに転送すべきか」を表す。
 */
export interface RouteEntry {
  /** 宛先ルータID。実環境では宛先ネットワーク（CIDR表記）に相当 */
  destination: string;
  /**
   * ネクストホップ（次に転送するルータID）。
   * パケットはここに送られ、以降はネクストホップのルーティングテーブルに従い転送される。
   * これがIPルーティングの核心: 各ルータは最終宛先ではなく「次の1ホップ」だけを決定する。
   */
  nextHop: string;
  /** 宛先までの総コスト（メトリック）。OSPFではリンクコストの合計値 */
  cost: number;
  /** 宛先までの完全な経路（経由するルータIDの列）。デバッグ・可視化用 */
  path: string[];
}

/**
 * リンク（2つのルータ間の物理接続）
 *
 * 実ネットワークではイーサネットケーブル、光ファイバー、海底ケーブル等に相当する。
 * コストはOSPFのリンクコスト（通常は帯域幅の逆数: 10^8 / 帯域bps）を模して設定されている。
 * AS間を接続するリンクはIX（Internet Exchange Point）を経由する。
 */
export interface Link {
  /** リンクの一意識別子。"L1"=AS内リンク、"IX1"=AS間リンク(インターネットエクスチェンジ) */
  id: string;
  /** 接続元ルータID */
  from: string;
  /** 接続先ルータID（双方向リンクなのでfrom/toは便宜上の区別） */
  to: string;
  /** リンクコスト（遅延ミリ秒で表現）。ルーティングのメトリックとして使用される */
  cost: number;
  /** 帯域幅の表示用文字列（例: "1Gbps", "100Gbps"）。コスト計算には使用しない */
  bandwidth: string;
}

/**
 * パケット
 *
 * IPパケットの簡易モデル。実際のIPヘッダは送信元IP、宛先IP、TTL、プロトコル番号等を含む。
 * TTL（Time To Live）はパケットがネットワーク上で無限ループするのを防ぐ仕組みで、
 * 各ルータを通過するたびに1減算され、0になるとパケットは破棄される。
 * LinuxのデフォルトTTLは64、WindowsのデフォルトTTLは128。
 */
export interface Packet {
  /** パケットの一意識別子（シミュレーション内での追跡用） */
  id: number;
  /** 送信元IPアドレス */
  srcIp: string;
  /** 宛先IPアドレス。各ルータはこのアドレスを見てルーティングテーブルを参照する */
  dstIp: string;
  /** 送信元ルータID */
  srcRouter: string;
  /** 宛先ルータID */
  dstRouter: string;
  /**
   * TTL (Time To Live)。
   * ルータを1つ通過するごとに1減算される。0になるとパケットを破棄し、
   * 送信元にICMP Time Exceededメッセージを返す（tracerouteコマンドの原理）。
   */
  ttl: number;
  /** ペイロード（表示用文字列）。実際にはHTTPリクエスト等のデータが入る */
  payload: string;
}

/**
 * ホップイベント（パケットが1ホップ進んだ記録）
 *
 * パケットがあるルータから次のルータに転送されるたびに1つ生成される。
 * tracerouteコマンドの出力に相当する情報を保持する。
 */
export interface HopEvent {
  /** 対応するパケットのID */
  packetId: number;
  /** 転送元ルータID（このルータがルーティング判断を行った） */
  fromRouter: string;
  /** 転送先ルータID（ネクストホップ） */
  toRouter: string;
  /** 使用されたリンクのID */
  linkId: string;
  /** このホップ通過後のTTL値 */
  ttl: number;
  /** このホップで使用されたルーティングテーブルエントリ。ルーティング判断の根拠 */
  routeEntry: RouteEntry | undefined;
  /** シミュレーション内のタイムスタンプ（リンクコストの累積値、ミリ秒） */
  timestamp: number;
}

/**
 * シミュレーション結果
 *
 * 1つのパケット送信に対する全ホップの記録と最終的な到達状況を保持する。
 */
export interface SimulationResult {
  /** 送信されたパケット */
  packet: Packet;
  /** パケットが経由した全ホップのイベント列 */
  hops: HopEvent[];
  /** パケットが宛先に到達したかどうか */
  delivered: boolean;
  /**
   * 結果の理由:
   * - "delivered": 正常に宛先に到達
   * - "ttl_exceeded": TTLが0になりパケット破棄（ループの可能性）
   * - "no_route": ルーティングテーブルに宛先への経路が存在しない
   * - "unknown_router": 指定されたルータIDが存在しない
   */
  reason: string;
}
